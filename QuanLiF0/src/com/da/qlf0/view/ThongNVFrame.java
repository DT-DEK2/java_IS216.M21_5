    private void jtfCMNDKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jtfCMNDKeyPressed
        // TODO add your handling code here:
         String sdt = jtfCMND.getText();
        int length = sdt.length();
        char c=evt.getKeyChar();
        if(evt.getKeyChar()>='0'&& evt.getKeyChar()<='9'){
            if(length<12){
                jtfCMND.setEditable(true);
            }else{
                jtfCMND.setEditable(false);
            }
        }else{
            if(evt.getExtendedKeyCode()==KeyEvent.VK_BACK_SPACE||evt.getExtendedKeyCode()==KeyEvent.VK_DELETE){
                jtfCMND.setEditable(true);
            }else{
                jtfCMND.setEditable(false);
            }
        }
    }//GEN-LAST:event_jtfCMNDKeyPressed

